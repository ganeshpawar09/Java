// Litrals

public class M_03_Litrals {
    public static void main(String[] args)
    {         
        byte age=34;
        char ch='A';
        float f1=5.6f; //f to make it float else by default it is double
        double d1=3.3324d;
        long num=233423423423432424l;
        boolean a=true;
        String str="Ganesh";

        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
        System.out.println(num);
        System.out.println(a);
        System.out.println(str);


    }
}
 